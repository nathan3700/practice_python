# Define a list of hobbies
hobbies = ["reading", "swimming", "gaming", "drawing"]

# Use a for loop to print each hobby
for hobby in hobbies:
    print("I enjoy " + hobby)

# Use an if statement to determine if a hobby is "reading"
if "reading" in hobbies:
    print("Reading is one of my hobbies.")
else:
    print("Reading is not one of my hobbies.")
